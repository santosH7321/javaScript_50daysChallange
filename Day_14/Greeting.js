//Q.  Write a function called 'greet' that takes a name parameter and logs a greeting message to the console.

// Define a function called greet that takes a name parameter
function greet(name) {
    // Use a console.log statement to output a greeting message with the name parameter
    console.log(`Hello ${name}! How are you?`);
  }
  
  // Call the greet function and pass in the name "Santosh" as an argument
  greet("Santosh"); // Output: Hello Pranay! How are you doing today?